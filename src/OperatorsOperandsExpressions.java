
public class OperatorsOperandsExpressions {

	public static void main(String[] args) {
		int result = 1+2; // 1+2=3
		System.out.println("1+2= "+result);
		result = result%2;
		System.out.println("result % 2= "+result);
		result = result/2;
		System.out.println("result/2= "+result);
		result++;
		System.out.println("result++ ="+result);
		result--;
		System.out.println("result-- ="+result);
		
	}

}
