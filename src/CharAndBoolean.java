
public class CharAndBoolean {

	public static void main(String[] args) {
		// We will talk about character or 'char' data type
		char myChar='D';
		char myUnicodeChar='\u0044'; // Cause we are typing in the unicode. You can find the code form a unicode table.
		System.out.println("My character: "+myChar);
		System.out.println("My character in Unicode: "+myUnicodeChar);
		
		//	We will talk a little about Boolean now.
		boolean myBoolTrue=true;
		boolean myBoolFalse=false;
		System.out.println("True Boolean Value: "+myBoolTrue);
		System.out.println("False Boolean Value: "+myBoolFalse);
		boolean isAgeAboveTwentyOne=true;
		System.out.println("Is the age of the given student above 21? : "+isAgeAboveTwentyOne);
		
	}

}
